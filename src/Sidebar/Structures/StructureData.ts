export interface ICoord {
    row: number;
    col: number;
}

export interface IStructure {
    cells: ICoord[],
    name: string
}

const gridToCells = (grid: number[][]): ICoord[] => {
    const o: ICoord[] = [];

    for(let row = 0; row < grid.length; row++) {
        for(let col = 0; col < grid[row].length; col++) {
            if(grid[row][col] === 1) o.push({row, col});
        }
    }

    return o;
}

